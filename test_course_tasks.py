# ============== ТЕСТЫ ДЛЯ check_email ==============
@pytest.mark.parametrize("email, expected", [
    ("Helloworld@.ru", True),
    ("мояпочта@нетология.ру", True),
    ("user@domain.com", True),
    ("name.surname@company.co.uk", True),
    ("python@email@net", False),      # две собаки
    (" em@il.ru", False),            # пробел в начале
    ("user@domain", False),          # нет точки
    ("user.domain.com", False),      # нет @
    ("user@.com", True),            # точка есть, @ есть – по нашему критерию True
    ("", False),
    ("   ", False),
])
def test_check_email(email, expected):
    assert check_email(email) == expected


# ============== ТЕСТЫ ДЛЯ solve ==============
@pytest.mark.parametrize("todo_list, workday, expected", [
    # Обычный случай
    ([["Разобрать почту", 1], ["Обзвонить клиентов", 2], ["Запланировать дела", 0.6],
      ["Сделать презентацию", 3], ["Созвон с командой", 0.5]], 8, 0.9),
    # Пустой список задач
    ([], 8, 8.0),
    # Одна задача
    ([["Написать отчёт", 1.5]], 8, 6.5),
    # Задачи с дробным временем
    ([["a", 0.1], ["b", 0.2], ["c", 0.3]], 1, 0.4),
    # Рабочий день меньше суммы задач → отрицательный результат
    ([["a", 5], ["b", 4]], 8, -1.0),
    # Рабочий день = 0
    ([["a", 0.5]], 0, -0.5),
    # Точность округления (1 знак)
    ([["a", 1.23], ["b", 2.34]], 4, 0.4),   # 4 - 3.57 = 0.43 -> round(0.43,1)=0.4
])
def test_solve(todo_list, workday, expected):
    assert solve(todo_list, workday) == expected


